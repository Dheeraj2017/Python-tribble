#!/usr/bin/python
import math

print("math.pow(100, 2) : ", math.pow(100, 2))
print("math.pow(100, -2) : ", math.pow(100, -2))
print("math.pow(2, 4) : ", math.pow(2, 4))
print("math.pow(3, 0) : ", math.pow(3, 0))
print("math.pow(-3,2) : ", math.pow(-3,2))
print("math.pow(-3,3) : ", math.pow(-3,3))
#this is the power function which is used using the math module and access by math.pow(base,exponent)
#both base and exponent can be positive or negative